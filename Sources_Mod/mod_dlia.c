//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | FIR滤波器
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | pf_Input: FIR采样点输入   uin_Lenth:采样点长度
//|          | pf_Factor: FIR系数        uin_Order: FIR阶数
//|          | uin_Avg: 平均滤波         uin_Spand: 结果数组缩放比例 5 = 1000个点缩放为200个点
//|----------|--------------------------------------------------------------------------------------
//| 返回参数 | 无
//|----------|--------------------------------------------------------------------------------------
//| 函数设计 |
